#IMPORTS
import datetime
import random

#GET USER-AGENT
def getUserAgent():

    platform = random.choice(
    ['Macintosh',
    'Windows',
    'X11']
    )

    if platform == 'Macintosh':
        os  = random.choice(
        ['68K',
        'PPC']
        )

    elif platform == 'Windows':
        os  = random.choice(
        ['Win3.11',
        'WinNT3.51',
        'WinNT4.0',
        'Windows NT 5.0',
        'Windows NT 5.1',
        'Windows NT 5.2',
        'Windows NT 6.0',
        'Windows NT 6.1',
        'Windows NT 6.2',
        'Win 9x 4.90',
        'Win95',
        'Win98',
        'WindowsCE']
        )

    elif platform == 'X11':
        os  = random.choice(['Linux i686', 'Linux x86_64'])

    browser = random.choice(['chrome', 'firefox', 'ie'])

    if browser == 'chrome':
        webkit = str(random.randint(500, 599))
        version = str(random.randint(0, 28)) + '.0' + str(random.randint(0, 1500)) + '.' + str(random.randint(0, 999))
        return 'Mozilla/5.0 (' + str(os) + ') AppleWebKit/' + webkit + '.0 (KHTML, like Gecko) Chrome/' + version + ' Safari/' + webkit

    elif browser == 'firefox':
        currentYear = datetime.date.today().year
        year = str(random.randint(2000, currentYear))
        month = random.randint(1, 12)

        if month < 10:
            month = '0' + str(month)
        else:
            month = str(month)

        day = random.randint(1, 30)

        if day < 10:
            day = '0' + str(day)

        else:
            day = str(day)

        gecko = year + month + day
        version = str(random.randint(1, 21)) + '.0'
        return 'Mozilla/5.0 (' + os + '; rv:' + version + ') Gecko/' + gecko + ' Firefox/' + version

    elif browser == 'ie':

        version = str(random.randint(1, 10)) + '.0'
        engine = str(random.randint(1, 5)) + '.0'
        option = random.choice(
        [True,
        False]
        )

        if option == True:
            token = random.choice(['.NET CLR', 'SV1', 'Tablet PC', 'Win64; IA64', 'Win64; x64', 'WOW64']) + '; '
        else:
            token = ''
        return 'Mozilla/5.0 (compatible; MSIE ' + version + '; ' + os + '; ' + token + 'Trident/' + engine + ')'
